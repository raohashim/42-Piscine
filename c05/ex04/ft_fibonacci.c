#include <stdio.h>

int	ft_fibonacci(int index)
{
	if (index < 0)
		return (-1);
	else if (index == 0)
		return (0);
	else if (index == 1 || index == 2)
	{
		return (1);
	}
	else
	{
		return (ft_fibonacci(index - 1) + ft_fibonacci(index - 2));
	}
}

int	main(void)
{
	int	a;
	int	b;

	a = 5;
	b = ft_fibonacci(a);
	printf("Result :: %d\n", b);
}
